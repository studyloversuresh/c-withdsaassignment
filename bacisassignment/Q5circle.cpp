// Q5. Write a program to find the circumference
// of a circle with radius 10 in C++.
#include<iostream>
using namespace std;
int main()
{
    float radius = 10;
    float py = 22/7;
    float circumference = 2*py*radius;
    cout<<"Radius of circle : "<<radius<<endl;
    cout<<"circumference of circle : "<<circumference<<endl;
    return 0;
}
